import { websiteUrl } from '@/lib/constants';
import { MetadataRoute } from 'next';

export default function robots(): MetadataRoute.Robots {
  return {
    rules: {
      userAgent: '*',
      allow: '/',
      disallow: '/private/',
    },
    sitemap: `${websiteUrl}/sitemap.xml`,
    host: `${websiteUrl}`,
  };
}
